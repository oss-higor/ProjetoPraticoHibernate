    public void pesquisaGeral(){
        
        String sql="SELECT c.CPF_, c.QUANTIDADE_, c.CODVENDA_, "
                + "v.status_venda, v.valor_total, v.data_venda, "
                + "v.cep_venda, v.estado_venda, v.cidade_venda, "
                + "v.bairro_venda, v.rua_venda, v.numero_venda "
                + "FROM tbcomprados as c INNER JOIN tbvenda as v ON v.cod_venda=c.CODVENDA_ ";
    
        try {
            pst = conexao1.prepareStatement(sql);
            rs = pst.executeQuery();
                
            jTableVendasExistentes.setModel(DbUtils.resultSetToTableModel(rs));
           
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    
    
    }
