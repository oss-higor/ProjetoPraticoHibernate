    public void pesquisaData(){
        int inicio =Integer.parseInt(Data_ini.getText());
        int fim =Integer.parseInt(Data_fim.getText());
        String sql="SELECT c.QUANTIDADE_, c.DESCRICAO_, "
                + "v.status_venda, v.valor_total, v.data_venda "
                + "FROM tbcomprados as c INNER JOIN tbvenda as v ON v.cod_venda=c.CODVENDA_ "
                + "WHERE v.data_venda BETWEEN "+inicio+" AND "+fim+"";
    
        try {
            pst = conexao1.prepareStatement(sql);
            rs = pst.executeQuery();
                
            Tabela_venda_data.setModel(DbUtils.resultSetToTableModel(rs));
           
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    
    
    }
